// ShortDramaTopics 从片名 + 简介里抽取短剧题材标签（去重、最多 6 个，按词表优先级）。
func ShortDramaTopics(name, overview string) []string {
	text := name + " " + overview
	out := make([]string, 0, 6)
	for _, t := range shortDramaTopics {
		for _, a := range t.Aliases {
			if strings.Contains(text, a) {
				out = append(out, t.Tag)
				break
			}
		}
		if len(out) >= 6 {
			break
		}
	}
	return out
}
